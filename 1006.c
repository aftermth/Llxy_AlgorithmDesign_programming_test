#include<stdio.h>
int main()
{
    char a,da,b,bd;
    int i=1,g=1,pa,pb,zf;
    a=getchar();
    da=getchar();
    b=getchar();
    bd=getchar();
    while('a'<='\n')
    {
        if('a'=='da')
            i++;
    }
    while('b'<='bd')
    {
        if('b'=='bd')
            g++;
    }
    pa=i*da;
    pb=g*bd;
    zf=pa+pb;
    printf("%d",zf);
    return 0;
}
